    private fun jTree1ValueChanged(
        evt: javax.swing.event.TreeSelectionEvent
    ) { //GEN-FIRST:event_jTree1ValueChanged
        val treepath: TreePath = evt.path
        val obj: Any? = treepath.lastPathComponent
        if (obj != null) {

            log.trace("mem: {}", Runtime.getRuntime().totalMemory())
            log.trace("free: {}", Runtime.getRuntime().freeMemory())

            val treenode = obj as SortedTreeNode
            if (treenode.root === treenode) {
                if (current_panel != null) {
                    displayPanel.removeAll()
                    current_panel = null
                }
                displayPanel.repaint()
            }
            val obj1: Any? = treenode.userObject
            if (obj1 is TreeNodeInfo) {
                val tmpnode: TreeNodeInfo = obj1
                val nodeobj: Graph = tmpnode.node_object
                if (current_panel != null) {
                    displayPanel.removeAll()
                    current_panel = null
                }
                mygraphview.setGraph(nodeobj)
                current_panel = mygraphview
                displayPanel.add(mygraphview)
                displayPanel.validate()
                displayPanel.repaint()

            }
            if (obj1 is ParentNodeInfo) {
                val tmpnode: ParentNodeInfo = obj1
                val nodeobj: List = tmpnode.node_object
                if (current_panel != null) {
                    displayPanel.removeAll()
                    current_panel = null
                }
                current_panel = nodeobj.run()
                displayPanel.add(current_panel)
                displayPanel.validate()
                displayPanel.repaint()
            }
        }

    } //GEN-LAST:event_jTree1ValueChanged

