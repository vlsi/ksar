    fun ask_treenode(node: SortedTreeNode, graphselection: GraphSelection) {
        val num = node.childCount

        if (num > 0) {
            val obj1: Any? = node.userObject
            if (obj1 is ParentNodeInfo) {
                val tmpnode: ParentNodeInfo = obj1
                val nodeobj: List = tmpnode.node_object
                askparentPanel = nodeobj.getprintform()
                graphselection.addPrintCheckBox(askparentPanel!!)
            }
            for (i in 0 until num) {
                val l = node.getChildAt(i) as SortedTreeNode
                ask_treenode(l, graphselection)
            }
            askparentPanel = null
        } else {
            val obj1: Any? = node.userObject
            if (obj1 is TreeNodeInfo) {
                val tmpnode: TreeNodeInfo = obj1
                val nodeobj: Graph = tmpnode.node_object
                val tmp: JCheckBox = nodeobj.getprintform()
                val askparentPanelLocal = askparentPanel
                if (askparentPanelLocal == null) {
                    graphselection.addPrintCheckBox(tmp)
                } else {
                    askparentPanelLocal.add(tmp)
                }
            }
        }
    }

