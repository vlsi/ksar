    private fun askSaveFilename(title: String, chdirto: File?): String? {
        var filename: String? = null
        val chooser = JFileChooser()
        chooser.dialogTitle = title
        if (chdirto != null) {
            chooser.currentDirectory = chdirto
        }
        val returnVal = chooser.showSaveDialog(GlobalOptions.uI)
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            filename = chooser.selectedFile.absolutePath
        }

        if (filename == null) {
            return null
        }

        if (File(filename).exists()) {
            val choix = arrayOf("Yes", "No")
            val resultat =
                JOptionPane.showOptionDialog(
                    null, "Overwrite $filename ?", "File Exist",
                    JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, choix,
                    choix[1]
                )
            if (resultat != 0) {
                return null
            }
        }
        return filename
    }

