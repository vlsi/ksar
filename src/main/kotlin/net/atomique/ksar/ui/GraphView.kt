    private fun csvButtonActionPerformed(
        evt: java.awt.event.ActionEvent
    ) { //GEN-FIRST:event_csvButtonActionPerformed
        val filename: String?
        val buffer: String

        filename = askSaveFilename("Export CSV", Config.getLastExportDirectory())

        if (filename == null) {
            return
        }
        Config.setLastExportDirectory(filename)
        if (!Config.getLastExportDirectory()!!.isDirectory) {
            Config.setLastExportDirectory(Config.getLastExportDirectory()!!.parentFile)
            Config.save()
        }

        buffer = thegraph!!.make_csv()

        log.debug(buffer)

        var out: BufferedWriter?
        try {
            out = BufferedWriter(FileWriter(filename))
        } catch (e: IOException) {
            out = null
        }
        if (out == null) {
            return
        }
        try {
            out.write(buffer)
            out.flush()
            out.close()
        } catch (e: IOException) {

        }


    } //GEN-LAST:event_csvButtonActionPerformed

