    private fun jComboBox1ActionPerformed(
        evt: java.awt.event.ActionEvent
    ) { //GEN-FIRST:event_jComboBox1ActionPerformed
        val source = evt.source
        var lafClassName: String? = null
        if (source === jComboBox1) {
            val comboBox = source as JComboBox<*>
            lafClassName = comboBox.selectedItem as String?
            if (lafClassName != null) {
                //String finalLafClassName = lafClassName;
                for (laf in UIManager.getInstalledLookAndFeels()) {
                    if (lafClassName == laf.name) {
                        try {
                            UIManager.setLookAndFeel(laf.className)
                            SwingUtilities.updateComponentTreeUI(GlobalOptions.uI)
                        } catch (e: Exception) {
                        }
                    }
                }
            }
        }
    } //GEN-LAST:event_jComboBox1ActionPerformed

    private fun OkButtonActionPerformed(
        evt: java.awt.event.ActionEvent
    ) { //GEN-FIRST:event_OkButtonActionPerformed
