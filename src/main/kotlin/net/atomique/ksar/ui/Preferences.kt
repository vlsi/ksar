        Config.pDFPageFormat = jComboBox2.selectedItem.toString()
        Config.imageHeight = jSpinner2.model.value as Int
        Config.imageWidth = jSpinner1.model.value as Int
        Config.linuxDateFormat = jComboBox3.selectedItem.toString()
        Config.save()
        dispose()
    } //GEN-LAST:event_OkButtonActionPerformed

