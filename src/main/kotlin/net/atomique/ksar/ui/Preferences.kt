    private fun load_landf() {
        val looks = UIManager.getInstalledLookAndFeels()
        var i = 0
        val n = looks.size
        while (i < n) {
            val tmp = looks[i].name
            UI_lanf_model.addElement(tmp)
            if (Config.landf == tmp) {
                jComboBox1.selectedItem = tmp
            }
            i++
        }
    }

